    static class Kontak {

        private String nama;
        private String nomor;

        public Kontak(String nama, String nomor) {
            this.nama = nama;
            this.nomor = nomor;
        }

        public String getNama() {
            return nama;
        }

        public String getNomor() {
            return nomor;
        }

        @Override
        public String toString() {
            return "Nama: " + nama + ", Nomor: " + nomor;
        }
    }

