    private void buttontambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttontambahActionPerformed
        // TODO add your handling code here:
        String namaKontak = nama.getText(); // variabel dan tipe data
        String nomorKontak = nomor.getText();

        if (!namaKontak.isEmpty() && !nomorKontak.isEmpty()) {
            daftarKontak.add(new Kontak(namaKontak, nomorKontak));
            nama.setText("");
            nomor.setText("");
            saveKontakToFile();
            tampilkanKontak();
        }
    }//GEN-LAST:event_buttontambahActionPerformed

    private void buttoncariActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttoncariActionPerformed
        // TODO add your handling code here:
        String cariNama = carinama.getText().toLowerCase();
        tableModel.setRowCount(0);

        for (Kontak kontak : daftarKontak) {
            if (kontak.getNama().toLowerCase().contains(cariNama)) {
                Object[] rowData = {kontak.getNama(), kontak.getNomor()};
                tableModel.addRow(rowData);
            }
        }
    }//GEN-LAST:event_buttoncariActionPerformed

    private void buttonhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonhapusActionPerformed
        // TODO add your handling code here:
        int selectedRow = tabelkontak.getSelectedRow();
        if (selectedRow >= 0) {
            daftarKontak.remove(selectedRow);
            tableModel.removeRow(selectedRow);
            saveKontakToFile();
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris yang ingin dihapus!");
        }
    }//GEN-LAST:event_buttonhapusActionPerformed

    private void filecsvActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_filecsvActionPerformed
        // TODO add your handling code here:
        try {
        // Dapatkan direktori dari path file CSV
        File file = new File(FILE_NAME);
        File directory = file.getParentFile();

        // Buka direktori menggunakan Desktop API
        if (directory != null && directory.exists() && Desktop.isDesktopSupported()) {
            Desktop.getDesktop().open(directory);
        }
    } catch (IOException e) {
        e.printStackTrace();
        // Tambahkan penanganan error jika perlu
    }
    }//GEN-LAST:event_filecsvActionPerformed
    private void tampilkanKontak() {
        tableModel.setRowCount(0);
        for (Kontak kontak : daftarKontak) {
            Object[] rowData = {kontak.getNama(), kontak.getNomor()};
            tableModel.addRow(rowData);
        }
    }

