    private void saveKontakToFile() {
    File directory = new File("C:\\kontakCSV");
    if (!directory.exists()) {
        directory.mkdir();
    }

    try (PrintWriter writer = new PrintWriter(new File(FILE_NAME))) {
        for (Kontak kontak : daftarKontak) {
            writer.println(kontak.getNama() + "," + kontak.getNomor());
        }
    } catch (FileNotFoundException e) {
        e.printStackTrace();
    }
}
    
    private void loadKontakFromFile() {
    daftarKontak.clear(); // Bersihkan daftar sebelum memuat
    try (Scanner scanner = new Scanner(new File(FILE_NAME))) {
        while (scanner.hasNextLine()) {
            String line = scanner.nextLine();
            String[] data = line.split(",");
            if (data.length == 2) {
                daftarKontak.add(new Kontak(data[0], data[1]));
            }
        }
    } catch (FileNotFoundException e) {
        // File belum ada, tidak masalah
    }
}
    
